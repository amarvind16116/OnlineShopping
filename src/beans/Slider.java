package beans;

public class Slider {
	
	private int id;
	private String img;
	public Slider() {
		super();
	}
	public Slider(String img) {
		this.img = img;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	
	
}
